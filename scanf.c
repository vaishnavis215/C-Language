#include<stdio.h>

void main()
{
	int a;
	float b;
	char c;
	
	printf("\n Enter int value:");
	scanf("%d",&a);
	printf("\n Enter float value:");
	scanf("%f",&b);
	printf("\n Enter character value:");
	scanf(" %c",&c);
	
	printf("\n value of int=%d",a);
	printf("\n value of float=%f",b);
	printf("\n value of character=%c",c);
}
